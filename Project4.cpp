#include <iostream>
using namespace std;

int main() {
    // Integer Types
    int a = 10;
    short b = 5;
    long c = 10000;
    long long d = 1000000;
    
    // Floating Point Types
    float x = 3.14f;
    double y = 3.14159265359;
    long double z = 2.718281828459045;
    
    // Character Type
    char letter = 'A';
    
    // Boolean Type
    bool isTrue = true;
    
    // Displaying values
    cout << "a (int): " << a << endl;
    cout << "b (short): " << b << endl;
    cout << "c (long): " << c << endl;
    cout << "d (long long): " << d << endl;
    cout << "x (float): " << x << endl;
    cout << "y (double): " << y << endl;
    cout << "z (long double): " << z << endl;
    cout << "letter (char): " << letter << endl;
    cout << "isTrue (bool): " << isTrue << endl;
    
    return 0;
}
